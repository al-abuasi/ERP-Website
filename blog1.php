	Summer has traditionally been the favored season for burglary and assault but today’s more sophisticated criminal is finding Fall ripe with opportunity. The change of routine that occurs in both homes and businesses as young people go back to school and the days get shorter often result in a lapse in diligence. <br><br>

	For many businesses, particularly retail, late Summer and early Fall are often the low point in annual cash flow. Consequently, thieves are like hungry buzzards watching for any opening like an obviously broken security camera or by-passed entry keypad. Delaying repairs, routine maintenance or software upgrades until after the holiday shopping season can frequently become a classic case of penny wise and pound foolish.    <br><br>

	Every change of season brings with it human and environmental elements which can adversely impact any security system large or small. That is why quarterly system inspection, testing and upgrading is becoming the new norm for the sincerely security conscious. <br><br>
